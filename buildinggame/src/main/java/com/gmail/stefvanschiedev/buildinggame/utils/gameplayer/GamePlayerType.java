package com.gmail.stefvanschiedev.buildinggame.utils.gameplayer;

/**
 * An enum containing the different game player types
 *
 * @since 2.1.0
 */
public enum GamePlayerType {

    /**
     * Represents a game player who is playing a game
     *
     * @since 2.1.0
     */
	PLAYER,

    /**
     * Represents a game player who is spectating a game
     *
     * @since 2.1.0
     */
	SPECTATOR
	
}