package com.gmail.stefvanschiedev.buildinggame.utils.arena;

/**
 * Represents the modes an arena can be in
 *
 * @since 2.1.0
 */
public enum ArenaMode {

    /**
     * Represents a free for all match; only one person a plot
     *
     * @since 2.1.0
     */
	SOLO,

    /**
     * Represents a team match; with as many people as you want
     */
	TEAM
	
}